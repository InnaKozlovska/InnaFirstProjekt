package roman.kotL02Zminni;

/*
byte = 8 bit
short = 16 bit
char = 16 bit
int = 32 bit
long = 64 bit


 */
public class L02Zminni {
    public static void main(String[] args) {
        byte b = -128;
        short s = -32768;
        char c = 'a';
        int i = 2147483647;
        long l = -2147483648L;
        boolean bool = true;
        float f = 3.14f;
        double d = 3.14;
        System.out.println(i);

        i = 99;
        System.out.println(i);

    }
}
