package roman.kotL08ForWhileDowhile;

public class L08Dowhile {
    public static void main(String[] args) {
        int i = 1;
        System.out.println("DOWHILE");
        do {
            System.out.println("Iteration number " + i);
            i++;
        } while (i <= 10);
        System.out.println("End!");

    }
}
